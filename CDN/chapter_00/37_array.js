// Number 타입으로만 이루어진 타입
let arr1 = [1,2,3,4,5];

// String 타입으로만 이루어진 타입
let arr2 = ["h","e","l","l","o"];

// Number 타입과 String 타입을 함께 사용한 배열
let arr3 = [1,"h",2,"i"];

// 다양한 자료형들을 함께 사용한 배열
let arr4 = [true, 1, undefined, false, "h", 2, null, "i"];

console.log(arr1[0]);
// 출력 결과: 1

console.log(arr2[1]);
// 출력 결과: e

console.log(arr3[2]);
// 출력 결과: 2

console.log(arr4[3]);
// 출력 결과: false